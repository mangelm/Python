def sumar(num1, num2):
    print("El resultado de la suma es:", num1+num2)

def restar(num1, num2):
    print("El resultado de la resta es:", num1-num2)

def multiplicar(num1, num2):
    print("El resultado de la multiplicación es:", num1*num2)

def dividir(dividiendo, divisor):
    print("El resultado de la división es:", dividiendo/divisor)

def potencia(base, exponente):
    print("El resultado de la potencia es:", base**exponente)

def redondear(num1):
    print("El numero redondeado es:", round(num1))